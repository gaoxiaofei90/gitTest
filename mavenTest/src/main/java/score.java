public class score {
    private float scoreId;
    private int score;
    private String name;

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getScoreId() {
        return scoreId;
    }

    public void setScoreId(float scoreId) {
        this.scoreId = scoreId;
    }
}
